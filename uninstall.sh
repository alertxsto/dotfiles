printf "${YELLOW}Backups (if any) are at: ${DIM}~/.dotfiles-backup/<timestamp>/${NC}\n"
read -rp "Continue? [y/N] " confirm
if [[ ! "$confirm" =~ ^[yY]$ ]]; then
    echo "Aborted."
    exit 1
fi

